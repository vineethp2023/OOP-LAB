import java.util.*;
class ConcateSample
{
	public static void main(String args[])
	{
		String st1;
		String st2;
		System.out.println("\n Enter two strings : ");
		Scanner sc=new Scanner(System.in);
		st1=sc.nextLine();
		st2=sc.nextLine();
		System.out.println("\n Concatenated string is: "+st1+" "+st2);
	}
}
